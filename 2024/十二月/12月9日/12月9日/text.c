//学会快捷键
//F5     启动调试，经常用来跳到下一个断点（逻辑上的下一个断点）
//F9     创建断点和取消断点，断点的重要作用，可以在程序的任意位置停止执行，继而一步步执行下去

//断点舍不得删掉---禁用断点
//怀疑是第五次循环出现了问题---断点（条件）


//F5和F9相互配合着用，在想要停下来的地方停下（即程序刷一下的运行到断点处，后面的代码就可以一步步的调试（监视）了
#include<stdio.h>
int main()
{
	int i = 0;
	int arr[10] = { 0 };

	for (i = 0;i < 10;i++)
	{
		arr[i] = i;
	}

	//如果已经确定了这个程序前面的代码没有错误，是后面出现了问题，如果还是按照之前的方法F10一步步调试
	//那么要调试前面循环的10次，循环10次后才能调试到下面的代码----这样的效率太低了
	//如下，如果在这里设置断点，程序运行后会直接快速运行到断点所在的语句，后面就可以一步一步
	//调试断点下面的语句了
	for (i = 0;i < 10;i++)
	{
		printf("%d", arr[i]);
	}
	return 0;
}

//F10    逐过程，通常用来处理一个过程，一个过程可以是一次函数调用，或者是一条语句（不能进入函数内部）
//F11    逐语句，就是每次都执行一条语句，但是这个快捷键可以使我们的执行逻辑进入函数内部（这是最常用的）
//CTRL+F5 开始不调试，如果你想让程序直接运行起来而不调试就可以直接使用


